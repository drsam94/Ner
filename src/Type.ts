
enum Type {
    Normal = 0,
    Fighting,
    Flying,
    Poison,
    Ground,
    Rock,
    Bug,
    Ghost,
    Steel,
    Fire,
    Water,
    Grass,
    Electric,
    Psychic,
    Ice,
    Dragon,
    Dark,
    Fairy
}

function typeToColor(type : Type) : string {
    switch (type) {
        case Type.Normal:
            return "gray";
        case Type.Fighting:
            return "coral";
        case Type.Flying:
            return "cadetblue";
        case Type.Poison:
            return "blueviolet";
        case Type.Ground:
            return "burlywood";
        case Type.Rock:
            return "darkkhaki";
        case Type.Bug:
            return "darkolivegreen";
        case Type.Ghost:
            return "mediumpurple";
        case Type.Steel:
            return "lightsteelblue";
        case Type.Fire:
            return "crimson";
        case Type.Water:
            return "deepskyblue";
        case Type.Grass:
            return "greenyellow";
        case Type.Electric:
            return "gold";
        case Type.Psychic:
            return "fuchsia";
        case Type.Ice:
            return "lightskyblue";
        case Type.Dragon:
            return "indigo";
        case Type.Dark:
            return "darkslategray";
        case Type.Fairy:
            return "hotpink";
    }
    return "";
}

